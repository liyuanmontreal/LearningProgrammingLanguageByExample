main() {
  // Strings can be appended using +
  print("dart" + "lang");

  // Integers and Floats
  print("1+1=${1+1}");
  print("7.0/3.0 =${7.0/3.0}");

  // Booleans
  print(true && false);
  print(false || true);
  print(!true);
}

